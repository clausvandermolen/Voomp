export const translations = { es, en, pt };

// Resolve a dot-notation key against a lang object, fallback to es
export function resolveKey(lang, key) {
  const lookup = (obj, k) => {
    const parts = k.split(".");
    let cur = obj;
    for (const p of parts) {
      if (cur == null || typeof cur !== "object") return undefined;
      cur = cur[p];
    }
    return cur;
  };
  const dict = translations[lang] ?? translations.es;
  const val = lookup(dict, key);
  if (val !== undefined) return val;
  if (lang !== "es") return lookup(translations.es, key) ?? key;
  return key;
}
